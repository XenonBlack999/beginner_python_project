#Knowledge
# π = 3.14159 (approx)

# Area of a Circle:        A = π * r**2
# Circumference:           C = 2 * π * r
# Diameter:                D = 2 * r
# Radius from Diameter:    r = D / 2


radius = float(input('Input your radius number (cm): '))

area = 3.14159 * radius * radius
print('The area of the circle is:', area, "cm²")


Circumference = 2 * 3.14159 * radius
print('The Circumference of the circle is:', Circumference, 'cm')

diameter = 2 * radius
print('The Diameter of the circle is :', diameter, 'cm')


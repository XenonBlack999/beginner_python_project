#shortcut operator in python 
# +=   Add and assign       → x += 1    is same as   x = x + 1
# -=   Subtract and assign  → x -= 1    is same as   x = x - 1
# *=   Multiply and assign  → x *= 2    is same as   x = x * 2
# /=   Divide and assign    → x /= 2    is same as   x = x / 2
# //=  Floor divide assign  → x //= 2   is same as   x = x // 2
# %=   Modulus and assign   → x %= 2    is same as   x = x % 2
# **=  Exponent and assign  → x **= 2   is same as   x = x ** 2

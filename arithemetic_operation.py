# Arithmatic Operator was +,-, %, //, /*, **






x = 4
y = 5

print('The value of x :', x)
print('The value of y :', y)


# +  Addition
print('By adding x and y:',x+y)

# -  Subtraction
print('By Subtraction y and x:',y-x)

# *  Multiplication
print('By Multiplication x and y:' , x*y)

# /  Division
print('By Division x and y:' , x/y)

# // Floor Division
print('By Floor Division x and y:' , x//y)

# %  Modulus
print('By Modulus x and y:' , x%y)

# ** Exponentiation
print('By Exponentiation x and y:' , x**y)